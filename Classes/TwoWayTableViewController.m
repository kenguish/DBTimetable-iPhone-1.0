// Customize the number of rows in the table view.
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [twoways count];
}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
	// Set up the cell...
	NSUInteger row = [indexPath row];
	/* NSUInteger section = [indexPath section]; */
	
	cell.textLabel.text = [(NSDictionary *)[twoways objectAtIndex: row] objectForKey: @"itemTitle"];
	cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;

    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
	NSUInteger row = [indexPath row];
	
	// Navigation logic may go here. Create and push another view controller.
	DetailViewController *nextController = 
		[[DetailViewController alloc] initWithNibName:@"DetailViewController" bundle:nil];
	
	nextController.filename = [(NSDictionary *)[twoways objectAtIndex: row] objectForKey: @"filename"];
	nextController.title = [(NSDictionary *)[twoways objectAtIndex: row] objectForKey: @"itemTitle"];
	
	[self.navigationController pushViewController:nextController animated: YES];
	[nextController release];
}


